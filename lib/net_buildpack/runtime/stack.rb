require 'rbconfig'
require 'net_buildpack/runtime'

module NETBuildpack::Runtime

  # Detects which stack the buildpack is running on 
  class Stack

    # Detects which stack / OS the buildpack container is running on 
    # @return [String, nil] returns +Unit | Windows+.
    def self.detect_stack
      @os ||= (
        host_os = RbConfig::CONFIG['host_os']
        case host_os
        when /mswin|msys|mingw|cygwin|bccwin|wince|emc/
          :windows
        when /darwin|mac os/
          :macosx
        when /linux/
          :linux
        when /solaris|bsd/
          :unix
        else
          :unix
        end
      )
      @os
    end
  end
end